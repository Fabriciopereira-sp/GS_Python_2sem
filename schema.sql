

-- TABELA 1: PROFESSIONALS (Cadastro do Funcionário e Nível de Risco)
CREATE TABLE PROFESSIONALS (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100),
    EMAIL VARCHAR2(150),
    ROLE VARCHAR2(100),
    SKILL_LEVEL NUMBER
);

-- TABELA 2: LEARNING_PATHS (Catálogo de Cursos / Trilhas de Aprendizado)
CREATE TABLE LEARNING_PATHS (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    TITLE VARCHAR2(150),
    DESCRIPTION VARCHAR2(500),
    DURATION_HOURS NUMBER
);

